package com.array;

public class StringExample {
	public static void main(String[] args) {

		String[] names = new String[4];

		names[0] = "Anil";
		names[1] = "Bharat";
		names[2] = "Chandu";
		names[3] = "Druv";

		for (int i = 0; i < names.length; i++) {
			System.out.println(names[i]);
		}
	}
}
